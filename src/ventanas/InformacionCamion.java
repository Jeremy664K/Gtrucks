    private void boton_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_modificarActionPerformed
        
        String conductor = txt_conductor.getText();
        String modelo = txt_modelo.getText();
        String marca = cmb_marca.getSelectedItem().toString();
        String estatus = cmb_estatus.getSelectedItem().toString();
        
        try {
            
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                "update camiones set conductor = ?, modelo = ?, marca = ?, estatus = ? where ID = '" + idCamion + "'");
            
            pst.setString(1, conductor);
            pst.setString(2, modelo);
            pst.setString(3, marca);
            pst.setString(4, estatus);
            
            pst.executeUpdate();

            cn.close();
            txt_conductor.setBackground(Color.green);
            txt_modelo.setBackground(Color.green);
            cmb_marca.setBackground(Color.green);
            
            JOptionPane.showMessageDialog(null, "Camion Modificado con Exito");
            this.dispose();
            
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al modificar camion, Contacte con el Administrador");
            System.err.println("Error al modificar camion: \n" + e);
        }
    }//GEN-LAST:event_boton_modificarActionPerformed

